$(document).on("pageinit", "#info-page", function () {


    //set up string for adding <li/>
    var li = "";
    
    //container for $li to be added
    $.each(data, function (i, data) {
        //add the <li> to "li" variable
        //note the use of += in the variable
        //meaning I'm adding to the existing data. not replacing it.
        //store index value in array as id of the <a> tag
       li  += '<li ><a href="#" id="' + i + '"  class="info-go" data-transition="flip" style="background:white;font-weight:600;color:grey;box-shadw:none" >' + '<img src="'+data.img+'"  width="80" height="80" style="background-color:white;box-shadow:0px 1px 3px rgba(100,100,100,0.49);border-radius:40px;"/>' + data.name.charAt(0).toUpperCase() + data.name.slice(1) +'</p>'+'<p style="color:red;margin-top:-5px">'+data.type.toUpperCase()+"  " +data.area.charAt(0).toUpperCase() + data.area.slice(1)+'<p></li>'
    });
 
    //append list to ul
    $("#thelist").append(li).promise().done(function () {
        //wait for append to finish - thats why you use a promise()
        //done() will run after append is done
        //add the click event for the redirection to happen to #details-page
        $(this).on("click", ".info-go", function (e) {
            e.preventDefault();
            //store the information in the next page's data
            $("#details-page").data("info", data[this.id]);
            //change the page # to second page. 
            //Now the URL in the address bar will read index.html#details-page
            //where #details-page is the "id" of the second page
            //we're gonna redirect to that now using changePage() method
            $.mobile.changePage("#details-page");
        });

        //refresh list to enhance its styling.
        $(this).listview("refresh");
    });
});
